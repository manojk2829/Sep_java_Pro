package all_Java_Practice;

public class Switch_Case {

	public static void main(String[] args){
		enter_month(8);
	}

	public static void  enter_month(int n){
		switch(n)
		{
		case 1:
			System.out.println("Jan 1 ");
			break;
			
		case 2:
			System.out.println("Feb 2 ");
			break;
			
		case 3:
			System.out.println("Mar 3 ");
			break;
			
		case 4:
			System.out.println("Apr 4 ");
			break;
		case 5:
			System.out.println("May 5 ");
			break;
			
		case 6:
			System.out.println("Jun 6 ");
			break;
			
		case 7:
			System.out.println("july 7 ");
			break;
			
		case 8:
			System.out.println("Aug 8 ");
			break;
		}
	}
}
